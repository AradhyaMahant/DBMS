create table CLIENT_MASTER_1(
CLIENTNO CHAR(6) CHECK(CLIENTNO like 'C%'),
NAME VARCHAR(20) NOT NULL,
ADDRESS1 VARCHAR(30),
ADRESS2 VARCHAR(30),
CITY VARCHAR(15),
PINCODE INT ,
STATE VARCHAR(15),
BALDUE DECIMAL(10,2),
PRIMARY KEY(CLIENTNO)
);
GO

create table PRODUCT_MASTER_1(
PRODUCTNO VARCHAR(6) CHECK(PRODUCTNO LIKE 'P%'),
DESCRIPTION Varchar(15) NOT NULL,
PROFITPERCENT Decimal(4,2) NOT NULL,
UNIT_MEASURE Varchar(10) NOT NULL,
QTYONHAND INT NOT NULL,
REORDERL_VL INT NOT NULL,
SELLPRICE DECIMAL(8,2) NOT NULL,
COSTPRICE DECIMAL(8,2) NOT NULL,
PRIMARY KEY(PRODUCTNO)
)
GO

CREATE TABLE SALESMAN_MASTER_1(
SALESMANNO Varchar(6) CHECK (SALESMANNO LIKE 'S%') NOT NULL,
SALESMANNAME Varchar(20) NOT NULL,
ADDRESS1 Varchar(30) NOT NULL,
ADDRESS2 Varchar(30),
CITY Varchar(20),
PINCODE int,
STATE Varchar(20),
SALAMT DECIMAL(8,2)NOT NULL,
TGTTOGET decimal(6,2) NOT NULL,
YTDSALES decimal(6,2) NOT NULL,
REMARKS Varchar(60),
PRIMARY KEY(SALESMANNO)
);
GO

